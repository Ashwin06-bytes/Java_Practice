public class typecasting {
    public static void main(String[] args){
        int a = 5;
        int b = 10;
        int c = a/b;

        System.out.println(c);

        float d = a/b;
        System.out.println(d);

        float e = (float) a / (float) b;
        System.out.println(e);

    }

    
}
